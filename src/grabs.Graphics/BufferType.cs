namespace grabs.Graphics;

/// <summary>
/// Represents various different buffer types that can be used for rendering.
/// </summary>
public enum BufferType
{
    /// <summary>
    /// Vertex buffer.
    /// </summary>
    Vertex,
    
    /// <summary>
    /// Index buffer.
    /// </summary>
    Index,
    
    /// <summary>
    /// Constant buffer. Also known as a Uniform buffer.
    /// </summary>
    Constant
}